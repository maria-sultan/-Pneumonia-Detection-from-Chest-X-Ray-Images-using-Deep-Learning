"""## Model C — CNN + Transformer Hybrid

**Architecture justification:**
- A small **CNN stem** (3 conv blocks with stride/pooling) reduces the 224x224 image to a compact feature map (e.g. 28x28xC), extracting local textures cheaply before the more expensive Transformer stage.
- The feature map is reshaped into a **sequence of patch embeddings** (each spatial location = one "token", projected to `embed_dim`).
- A **learnable positional embedding** is added (Transformers have no inherent notion of spatial order).
- **Transformer encoder blocks** (Multi-Head Self-Attention + residual connections + LayerNorm + feed-forward MLP) let the model learn **global relationships** between distant regions of the lung (e.g. bilateral infiltrates), which pure CNNs with small receptive fields can miss.
- **GlobalAveragePooling1D** over the token sequence -> Dense classifier head.
- Residual connections + LayerNorm in the Transformer blocks help control **vanishing gradients** in this deeper, mixed architecture (Phase 4).

"""

class TransformerEncoderBlock(layers.Layer):
    def __init__(self, embed_dim, num_heads, ff_dim, dropout=0.1, **kwargs):
        super().__init__(**kwargs)
        self.att = layers.MultiHeadAttention(num_heads=num_heads, key_dim=embed_dim // num_heads)
        self.ffn = models.Sequential([
            layers.Dense(ff_dim, activation="relu"),
            layers.Dense(embed_dim),
        ])
        self.layernorm1 = layers.LayerNormalization(epsilon=1e-6)
        self.layernorm2 = layers.LayerNormalization(epsilon=1e-6)
        self.dropout1 = layers.Dropout(dropout)
        self.dropout2 = layers.Dropout(dropout)

    def call(self, inputs, training=False):
        attn_output = self.att(inputs, inputs)
        attn_output = self.dropout1(attn_output, training=training)
        out1 = self.layernorm1(inputs + attn_output)          # residual connection 1
        ffn_output = self.ffn(out1)
        ffn_output = self.dropout2(ffn_output, training=training)
        return self.layernorm2(out1 + ffn_output)             # residual connection 2


def build_cnn_transformer(input_shape=(IMG_SIZE, IMG_SIZE, 3),
                           embed_dim=128, num_heads=4, ff_dim=256,
                           num_transformer_blocks=2):
    inputs = layers.Input(shape=input_shape)

    # ---- CNN stem (local feature extraction) ----
    x = layers.Conv2D(32, 3, strides=2, padding="same", activation="relu")(inputs)  # 112x112
    x = layers.BatchNormalization()(x)
    x = layers.Conv2D(64, 3, strides=2, padding="same", activation="relu")(x)        # 56x56
    x = layers.BatchNormalization()(x)
    x = layers.Conv2D(embed_dim, 3, strides=2, padding="same", activation="relu")(x) # 28x28xembed_dim
    x = layers.BatchNormalization()(x)

    # ---- Reshape feature map into a sequence of patch tokens ----
    h, w, c = x.shape[1], x.shape[2], x.shape[3]
    num_patches = h * w
    x = layers.Reshape((num_patches, c))(x)

    # ---- Learnable positional embedding ----
    positions = tf.range(start=0, limit=num_patches, delta=1)
    pos_embedding = layers.Embedding(input_dim=num_patches, output_dim=embed_dim)(positions)
    x = x + pos_embedding

    # ---- Transformer encoder blocks ----
    for _ in range(num_transformer_blocks):
        x = TransformerEncoderBlock(embed_dim, num_heads, ff_dim, dropout=0.1)(x)

    # ---- Classification head ----
    x = layers.GlobalAveragePooling1D()(x)
    x = layers.Dropout(0.4)(x)
    x = layers.Dense(128, activation="relu",
                      kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.3)(x)
    outputs = layers.Dense(1, activation="sigmoid")(x)

    model = models.Model(inputs, outputs, name="cnn_transformer_hybrid")
    return model

cnn_transformer = build_cnn_transformer()

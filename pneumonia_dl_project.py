"""---
# PHASE 2 — Model Design and Implementation

We design and implement **three** architectures for the same binary classification task (Normal vs Pneumonia), so that Phase 3/4 can perform a fair analytical comparison.

| Model | Type | Pretrained? | Key idea |
|---|---|---|---|
| **Model A** | Custom CNN | No (from scratch) | Baseline convolutional feature extractor designed specifically for this dataset |
| **Model B** | ResNet50 | Yes (ImageNet) | Transfer learning — deep residual features, fine-tuned head |
| **Model C** | CNN + Transformer (hybrid) | No (from scratch) | CNN stem extracts local features; Transformer encoder models global/long-range dependencies between feature-map patches |

## Model A — Custom CNN (from scratch)

**Architecture justification:**
- 4 convolutional blocks (`Conv2D -> BatchNorm -> ReLU -> MaxPool`) with increasing filter counts (32 -> 64 -> 128 -> 256). Increasing depth lets the network learn from low-level edges/textures to high-level lesion/opacity patterns typical of pneumonia consolidation.
- **BatchNorm** after every conv layer stabilizes training and helps mitigate **vanishing/exploding gradients** (Phase 4 requirement).
- **ReLU** activations avoid vanishing gradients common with sigmoid/tanh in deep stacks.
- **GlobalAveragePooling2D** instead of Flatten + huge Dense layer — drastically reduces parameter count and overfitting risk.
- **Dropout (0.5)** before the final classifier — addresses overfitting (Phase 4).
- Output: single sigmoid neuron for binary classification (Normal=0, Pneumonia=1).
"""

def build_custom_cnn(input_shape=(IMG_SIZE, IMG_SIZE, 3)):
    inputs = layers.Input(shape=input_shape)

    x = layers.Conv2D(32, 3, padding="same", activation=None)(inputs)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    x = layers.MaxPooling2D()(x)

    x = layers.Conv2D(64, 3, padding="same")(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    x = layers.MaxPooling2D()(x)

    x = layers.Conv2D(128, 3, padding="same")(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    x = layers.MaxPooling2D()(x)

    x = layers.Conv2D(256, 3, padding="same")(x)
    x = layers.BatchNormalization()(x)
    x = layers.Activation("relu")(x)
    x = layers.MaxPooling2D()(x)

    x = layers.GlobalAveragePooling2D()(x)
    x = layers.Dropout(0.5)(x)
    x = layers.Dense(128, activation="relu",
                      kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.3)(x)
    outputs = layers.Dense(1, activation="sigmoid")(x)

    model = models.Model(inputs, outputs, name="custom_cnn")
    return model

custom_cnn = build_custom_cnn()
custom_cnn.summary()

"""## Model B — ResNet50 (Transfer Learning)

**Architecture justification:**
- Backbone: `ResNet50` pretrained on ImageNet. The residual ("skip") connections allow training of very deep networks without **vanishing gradients** — directly relevant to Phase 4's "vanishing gradient" mitigation discussion.
- The convolutional base is **frozen initially** (feature extraction mode) so we reuse low-level visual features (edges, blobs, textures) learned from millions of natural images — useful even for X-rays at the early-layer level.
- A new classification head is added: `GlobalAveragePooling2D -> Dropout -> Dense(128, relu) -> Dropout -> Dense(1, sigmoid)`.
- Optionally, the **top layers can be unfrozen and fine-tuned** at a low learning rate after initial training — a standard transfer-learning practice that improves domain adaptation to medical images.

"""

def build_resnet50(input_shape=(IMG_SIZE, IMG_SIZE, 3), fine_tune_at=None):
    base_model = keras.applications.ResNet50(
        include_top=False, weights="imagenet", input_shape=input_shape
    )
    base_model.trainable = False  # freeze for initial training (feature extraction)

    if fine_tune_at is not None:
        base_model.trainable = True
        for layer in base_model.layers[:fine_tune_at]:
            layer.trainable = False

    inputs = layers.Input(shape=input_shape)
    x = base_model(inputs, training=False)
    x = layers.GlobalAveragePooling2D()(x)
    x = layers.Dropout(0.5)(x)
    x = layers.Dense(128, activation="relu",
                      kernel_regularizer=regularizers.l2(1e-4))(x)
    x = layers.Dropout(0.3)(x)
    outputs = layers.Dense(1, activation="sigmoid")(x)

    model = models.Model(inputs, outputs, name="resnet50_transfer")
    return model, base_model

resnet_model, resnet_base = build_resnet50()

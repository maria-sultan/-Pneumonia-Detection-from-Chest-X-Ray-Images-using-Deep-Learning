"""### 1.8 Preprocessing pipeline

- **Resize** every image to `224 x 224` (standard input size compatible with ResNet50 and giving enough spatial resolution for the Transformer patch embedding).
- Convert grayscale X-rays to **3 channels** (required by ImageNet-pretrained ResNet50).
- **Normalize** pixel values to `[0, 1]`.
- **Augmentation** (training set only): random horizontal flip, small rotation, zoom, and brightness/contrast jitter — this directly targets **overfitting** (Phase 4) given the relatively small dataset and class imbalance.

"""

IMG_SIZE = 224
BATCH_SIZE = 32
LABEL_MAP = {"NORMAL": 0, "PNEUMONIA": 1}

def load_and_preprocess(filepath, label, augment=False):
    img = tf.io.read_file(filepath)
    img = tf.image.decode_jpeg(img, channels=3)
    img = tf.image.resize(img, [IMG_SIZE, IMG_SIZE])
    img = tf.cast(img, tf.float32) / 255.0
    if augment:
        img = tf.image.random_flip_left_right(img)
        img = tf.image.random_brightness(img, max_delta=0.1)
        img = tf.image.random_contrast(img, lower=0.9, upper=1.1)
        # small random rotation via random crop/zoom
        img = tf.image.resize_with_crop_or_pad(img, IMG_SIZE + 20, IMG_SIZE + 20)
        img = tf.image.random_crop(img, [IMG_SIZE, IMG_SIZE, 3])
    return img, label

def make_dataset(dataframe, augment=False, shuffle=False, batch_size=BATCH_SIZE):
    filepaths = dataframe["filepath"].values
    labels = dataframe["label"].map(LABEL_MAP).values.astype("int32")
    ds = tf.data.Dataset.from_tensor_slices((filepaths, labels))
    if shuffle:
        ds = ds.shuffle(buffer_size=len(filepaths), seed=SEED)
    ds = ds.map(lambda x, y: load_and_preprocess(x, y, augment=augment),
                num_parallel_calls=tf.data.AUTOTUNE)
    ds = ds.batch(batch_size).prefetch(tf.data.AUTOTUNE)
    return ds

